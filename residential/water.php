			<h3>Overview</h3>

			<p>Water damage in your home can come from a variety of sources. In Indiana, we often think about sump pump failures and a flooded basement. However, water damage from other sources can potentially be just as devastating. Broken pipes, leaking refrigerators and ice makers, frozen spigots, washing machine hoses, sewage backups, roof leaks and ice dams can all cause significant damage to the inside of your home. In most of these situations, it is important to rely on a professional in the field to protect the integrity and security of your investment. Indiana Restoration &amp; Cleaning Services has certified water damage specialists that can thoroughly inspect your property and work with your insurance company to ensure your home is properly dried.</p>

			<h3>Services</h3>

			<p class="note"><i>Indiana Restoration &amp; Cleaning Services follows standards for water damage restoration as set forth by the <abbr title="Institute for Inspection, Cleaning and Restoration Certification">IICRC</abbr>&rsquo;s S-500 Water Damage Mitigation Standard.</i></p>

			<dl>

				<dt>Inspection and Evaluation</dt>
				<dd>In any water damage situation, it is important to assess the building materials and contents that are retaining moisture and develop a specific and unique drying plan for each loss. We have trained personnel and specialty equipment that can evaluate water damage and ensure all areas that are retaining moisture are identified and addressed. Most often during a water loss situation, there are more materials that are wet than appear to be. Even if you feel the water damage is small or isolated, it should be evaluated by a water damage restoration professional.</dd>
				
				<dt>Extraction</dt>
				<dd>Removing excess moisture in a water damage situation is a critical first step in the water damage restoration process. We have the appropriate equipment to ensure this excess moisture is removed quickly and thoroughly.</dd>
				
				<dt>Structural Drying</dt>
				<dd>Even after the excess (or visible) moisture is removed after a water damage situation, building materials and contents normally retain some level of moisture and need to be further addressed through drying. If the structure is not appropriately dried, over a period of time, odors, bacteria and mold could result. We have trained professionals and specialty drying equipment to thoroughly dry your property and protect your investment.</dd>
				
				<dt>Building Material Removal</dt>
				<dd>Some building materials can be dried in place through air movement and dehumidification. Unfortunately there are times when building materials cannot be appropriately dried and need to be removed. We have trained professionals that know the proper way to remove only the necessary building materials to get your property fully dried. Removing only what is necessary facilitates a faster rebuilding process and getting your investment back to its pre-loss condition. If building materials need to be removed during the drying process, we have the resources and expertise to rebuild or repair your property.</dd>
				
				<dt>Deodorization, Cleaning and Antimicrobials</dt>
				<dd>When water damage occurs, often times it is accompanied by the presence of a variety of odors. While this is quite normal, the presence of &ldquo;musty&rdquo; odors is due to the presence of odor-causing bacteria. We have the expertise to ensure your investment is protected from odors and we will clean and treat your property and possessions to eliminate these odors if necessary.</dd>
				
				<dt>Contents Restoration</dt>
				<dd>Damage to your personal property can often be emotional. We pride ourselves in caring for all of your personal belongings as if they were our own. In many cases we can clean and restore your property at your home. In the event that your home has significant damage and your personal property must be removed, we carefully pack your personal property and move it to our secure facility for processing and cleaning. After it has been fully cleaned and deodorized it is safely stored in crates at our facility where it awaits delivery back to your home following successful repairs and remediation.</dd>
				
				<dt>Repair and Rebuild</dt>
				<dd>In some cases, a water damage loss can cause significant damage to drywall, floors and even mechanical systems in the home. As a full service restoration contractor, we can repair your home from a water damage incident to its pre-loss condition. We have the experience and knowledge in working with your insurance company to ensure your investment is restored with top quality building materials and craftsmanship.</dd>

